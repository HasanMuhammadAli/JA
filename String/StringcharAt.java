package String;

public class StringcharAt {
    public static void main(String[] args) {
        //charAt method in String class
        //This method returns the character of the specific index
        String str = "Hello Guys!!";
        System.out.println("Character at index 4 in str String: "+str.charAt(4));

        System.out.println("Character at index 6 in str String: "+str.charAt(6));
        
    }
}
